package bootcamp.giselle.operadores;

public class Operadores4 {
    // incremetar e decrementar
    public static void main(String[] args) {
        int numero = 5;

        numero++;

        System.out.println(numero);

        numero --;

        System.out.println(numero);

        // negacao

        boolean isTrue = true;
        isTrue = !isTrue;

        System.out.println(isTrue);
    }
}
